""" Operaciones Aritméticas (Enteros y Decimales)"""

# + (Suma)
5  +  2  # 7
"he" + "llo" # "hello"

# - (Resta)
6  -  2  # 4

# * (Multiplicación)
4  *  2  # 8
"ja"*3   # "jajaja"

# / (División) --- float
8  /  5  # 1.6

# // (División Entera)
8  // 5  # 1

# % (Módulo o Residuo)
8  %  5  # 3

# ** (Potencia y Raíz) --- float
3  ** 2  # 9
9  ** (1/2) # 3.0 


""" Operaciones Bit por Bit (Bitwise) """

50         # bin(0011 0010)
25         # bin(0001 1001)

# &
50 & 25    # bin(0001 0000) == 16

# |
50 | 25    # bin(0011 1011) == 59

# ^
50 ^ 25    # bin(0010 1011) == 43

#~
~50        # -(50 + 1)

# >>
50 >> 1    # bin(0011 0010) >> 1 == bin(0001 1001) == 25

# <<
50 << 2    # bin(0011 0010) << 2 == bin(1100 1000) == 200









