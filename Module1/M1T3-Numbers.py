""" Numbers """

""" Print and Input """
print("Bienvenido a la calculadora")

num1 = float(input("Digite el primer número:\n>"))
num2 = float(input("Digite el segundo número:\n>"))

""" Operaciones aritméticas """

#Op1. Suma
print("Suma:")
suma = num1 + num2
print(suma)

#Op2. Resta
print("Resta:")
resta = num1 - num2
print(resta)

#Op3. Multiplicación
print("Multiplicación")
multiplicacion = num1 * num2
print(multiplicacion)

#Op4. División
print("División")
division = num1 / num2
print(division)

#Op5. Potencia
print("Potencia")
potencia = num1 ** num2
print(potencia)

#Op6. División Entera
print("División Entera")
divEntera = num1 // num2
print(divEntera)

#Op7. Módulo
print("Módulo")
modulo = num1 % num2
print(modulo)
